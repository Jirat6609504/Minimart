    private void updateProductFromFile() {
        try {
            File file = new File(txtLocation.getText());
            Scanner scanner = new Scanner(file);

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0); // ล้างข้อมูลในตารางก่อนการอัปเดต

            while (scanner.hasNextLine()) {
                String line = scanner.nextLine();
                    String[] parts = line.split(" ");
                    // ตรวจสอบว่า parts มีจำนวนส่วนที่เพียงพอหรือไม่
                    if (parts.length >= 4) {
                        model.addRow(new Object[]{parts[0], parts[1], parts[2], parts[3]});
                    } else {
                        // กรณีข้อมูลไม่เพียงพอ
                        System.out.println("Invalid data: " + line);
                    }
            }

            scanner.close();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "File not found: " + e.getMessage());
        }
    }
