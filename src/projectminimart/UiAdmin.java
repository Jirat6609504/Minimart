    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable2.getSelectedRow();
    if (selectedRow != -1) {
                DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
                // ดึงข้อมูลจากแถวที่เลือกและนำไปแสดงในช่องข้อมูล
                txtNP.setText(model.getValueAt(selectedRow, 0).toString());
                txtPrice.setText(model.getValueAt(selectedRow, 1).toString());
                txtQuantity.setText(model.getValueAt(selectedRow, 2).toString());
                String imagePath = model.getValueAt(selectedRow, 3).toString(); // ข้อมูลรูปภาพเป็นสตริง (ที่อยู่ของไฟล์รูปภาพ)
                if (imagePath != null && !imagePath.isEmpty()) {
                    try {
                        // อ่านไฟล์รูปภาพจากที่อยู่ที่ระบุ
                        BufferedImage image = ImageIO.read(new File(imagePath));
                        // แปลง BufferedImage เป็น ImageIcon
                        ImageIcon imageIcon = new ImageIcon(image);
                        // ตั้งค่ารูปภาพใน JLabel
                        jLabel10.setIcon(imageIcon);
                    } catch (IOException ex) {
                        // กรณีเกิดข้อผิดพลาดในการอ่านไฟล์รูปภาพ
                        ex.printStackTrace();
                        JOptionPane.showMessageDialog(rootPane, "Error loading image: " + ex.getMessage());
                    }
                } else {
                    jLabel10.setIcon(null); // ล้างรูปภาพที่แสดงใน jLabel10 หากไม่มีข้อมูลรูปภาพ
                }
                            }
    }//GEN-LAST:event_jTable2MouseClicked

