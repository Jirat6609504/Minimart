    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // TODO add your handling code her
        
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        String nameProduct = txtNP.getText().trim();
        String priceText = txtPrice.getText().trim();
        String quantityText = txtQuantity.getText().trim();

        if (nameProduct.isEmpty() || priceText.isEmpty() || quantityText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            txtNP.requestFocus();
        } else {
            int price = Integer.parseInt(priceText);
            int quantity = Integer.parseInt(quantityText);
            model.addRow(new Object[]{nameProduct, price, quantity});
            // ตรวจสอบว่ามีภาพที่เลือกและแสดงใน JLabel10 หรือไม่
            if (jLabel10.getIcon() != null) {
                // นำข้อมูลรูปภาพเข้าสู่ตาราง
                ImageIcon imageIcon = (ImageIcon) jLabel10.getIcon();
                model.setValueAt(imageIcon, model.getRowCount() - 1, 3); // ตั้งค่ารูปภาพในคอลัมน์สุดท้ายของแถวที่เพิ่มล่าสุด
            }
        }
    }//GEN-LAST:event_btninsertActionPerformed

